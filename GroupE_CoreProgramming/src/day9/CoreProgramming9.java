package day9;

public class CoreProgramming9 {

	public static void main(String[] args) {
		//Operators
		//Assignments, Arithmetic, Relational, Conditional, Increment/Decrement
		//Logical Operators &&, ||, !
		
		//Logical AND &&
		//Logical OR ||
		//Compares multiple conditions, and return boolean result
		//(Value1 ConditonalOperator Value2) -> Condition
		boolean result = (4>2) && (4>0); //AND
		System.out.println(result); //true
		result = (4>9) && (4>0); //AND
		System.out.println(result); //false
		
		//Logical OR ||
		result = (4>20) || (4>0); //OR
		System.out.println(result); //true
		
		//Logical NOT !
		//Convert true to false/false to true
		result=false;
		result=!result;
		System.out.println(result);
		
		//Other operators
		//{}, [], (), ,, .
		
		//Data types
		//Primitive Type/Basic Type
		//byte, short, int, long, float, double, char, boolean
		
		//Reference Type/Class Type
		//String, Math, User defined classes (Person, Calculator)
		//Array
		
		
		
		
	}

}










