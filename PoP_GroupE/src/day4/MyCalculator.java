package day4;

public class MyCalculator {
	//Fields
	int num1;
	int num2;
	int num3;
	
	//Methods
	//Create a method which calculate the product of num1, and num2
	void calcPrd() {
		//Calculate product
		this.num3=this.num1*this.num2;
	}
	
	//Create a method which calculate the difference of num1, and num2
	void calcDif() {
		//Calculate diff
		this.num3=this.num1-this.num2;
	}
	void calcSum() {
		//Calculate sum
		this.num3=this.num1+this.num2;
	}
}
