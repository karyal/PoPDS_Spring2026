package day13;

public class SwitchCaseTest {

	public static void main(String[] args) {
		// Enter any value: 5
		// THU
		// switch case
		int day = 3;
		switch (day) {
		case 1:
			System.out.println("SUN");
			break;
		case 2:
			System.out.println("MON");
			break;
		default:
			System.out.println("Out of range");
		}
		
	}

}
